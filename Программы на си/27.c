//Выведите на экран таблицу значений синуса от 0 до 2.В каждой строке должны
//стоять один аргумент и одно значение.Количество значений аргумента пусть задает
//пользователь.
#include <stdio.h>
#include <math.h>
#include <locale.h>


#define PI 3.14159265358979323846

int main() {
    char* locale = setlocale(LC_ALL, "rus");
    int n; // Количество значений аргумента
    printf("Введите количество значений аргумента: ");
    scanf("%d", &n);

    // Проверка на корректность ввода
    if (n <= 0) {
        printf("Количество значений должно быть положительным.\n");
        return 1;
    }

    // Вывод заголовка таблицы
    printf("\nАргумент (x) | Синус (sin(x))\n");
    printf("-------------------------------\n");

    // Вычисление и вывод значений
    for (int i = 0; i <= n; i++) {
        double x = (2 * PI * i) / n; // Расчет аргумента
        double sinValue = sin(x);     // Вычисление синуса
        printf("%12.6f | %12.6f\n", x, sinValue); // Вывод результата
    }

    return 0;
}
