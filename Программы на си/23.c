//Напишите программу, которая в зависимости от введенного возраста добавляет
//слова «год», «года» или «лет».Например, при вводе возраста 1, программа сообщает
//«1 год», при числе 2 – «2 года», при числе 125 – «125 лет».
#pragma warning(disable : 4996)
#include <stdio.h>
#include <locale.h>

int main() {
    // Устанавливаем локаль для корректного отображения русского языка
    setlocale(LC_ALL, "RUS");

    int age;

    // Ввод возраста
    printf("Введите ваш возраст: ");

    // Проверка успешности ввода
    if (scanf("%i", &age) != 1) {
        printf("Ошибка ввода. Пожалуйста, введите числовое значение.\n");
        return 1; // Завершаем программу с ошибкой
    }

    // Проверка корректности возраста
    if (age < 0) {
        printf("Возраст не может быть отрицательным.\n");
        return 1; // Завершаем программу с ошибкой
    }

    // Определение правильной формы слова
    if (age % 10 == 1 && age % 100 != 11) {
        printf("%d год\n", age);
    }
    else if ((age % 10 >= 2 && age % 10 <= 4) && (age % 100 < 10 || age % 100 >= 20)) {
        printf("%d года\n", age);
    }
    else {
        printf("%d лет\n", age);
    }

    return 0; // Завершение программы без ошибок
}

