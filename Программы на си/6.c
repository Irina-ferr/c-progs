//Ротор двигателя вращается с постоянной скоростью, совершая n оборотов в секунду. Каков будет угол поворота через t секунд, если угол поворота ротора в нулевой момент времени принять за 0?
#include <stdio.h>

int main() {
    double n; // количество оборотов в секунду
    double t; // время в секундах
    double angle; // угол поворота

    // ввод количества оборотов и времени
    printf("Введите количество оборотов в секунду (n): ");
    scanf("%lf", &n);

    printf("Введите время в секундах (t): ");
    scanf("%lf", &t);

    // вычисляем угол поворота
    angle = n * 360.0 * t;

    // результат
    printf("Угол поворота через %.2f секунд: %.2f градусов\n", t, angle);

    return 0;
}
