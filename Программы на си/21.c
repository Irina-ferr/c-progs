//Напишите программу для расчета корней квадратного уравнения с проверкой
//неотрицательности дискриминанта.Если дискриминант отрицательный, сообщайте
//пользователю, что уравнение не имеет корней.Добавьте проверку того, что первый
//коэффициент не равен нулю.В противном случае сообщайте пользователю, что
//уравнение не квадратное, а линейное, и вычислите его единственный корень.Если
//первые два коэффициента равны нулю, а третий не равен, сообщите пользователю,
//что корней нет.А если все коэффициенты равны нулю, сообщите, что любое число
//является корнем.#include <stdio.h>

int main() {
    double a, b, c;
    printf("Введите коэффициенты a, b и c (ax^2 + bx + c = 0):\n");
    scanf("%lf %lf %lf", &a, &b, &c);

    if (a == 0 && b == 0 && c == 0) {
        printf("Любое число является корнем уравнения.\n");
    }
    else if (a == 0 && b == 0) {
        printf("Уравнение не имеет корней.\n");
    }
    else if (a == 0) {
        // Линейное уравнение bx + c = 0
        double root = -c / b;
        printf("Уравнение линейное. Его единственный корень: x = %.2lf\n", root);
    }
    else {
        // Квадратное уравнение ax^2 + bx + c = 0
        double discriminant = b * b - 4 * a * c;

        if (discriminant < 0) {
            printf("Уравнение не имеет корней.\n");
        }
        else if (discriminant == 0) {
            double root = -b / (2 * a);
            printf("Уравнение имеет один корень: x = %.2lf\n", root);
        }
        else {
            double root1 = (-b + sqrt(discriminant)) / (2 * a);
            double root2 = (-b - sqrt(discriminant)) / (2 * a);
            printf("Уравнение имеет два корня: x1 = %.2lf, x2 = %.2lf\n", root1, root2);
        }
    }

    return 0;
}
