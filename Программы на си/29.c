/*Выведите на экран последовательность сумм чисел от 1 до n; n меняется от 1 до 10.
Т.е.первые члены последовательности – 1, 3 (1 + 2), 6 (1 + 2 + 3), 10 (1 + 2 + 3 + 4) и т.д.*/
#include <stdio.h>
#include <locale.h>
 #pragma warning(disable : 4996)

int main() {
    char* locale = setlocale(LC_ALL, "rus");

    int sum = 0; // переменная для хранения текущей суммы

    printf("Последовательность сумм чисел от 1 до n (где n изменяется от 1 до 10):\n");

    // цикл от 1 до 10
    for (int n = 1; n <= 10; n++) {
        sum += n; // добавляем текущее значение n к сумме
        printf("%d ", sum); // выводим текущую сумму
    }

    printf("\n"); // переход на новую строку после вывода

    return 0;
}
