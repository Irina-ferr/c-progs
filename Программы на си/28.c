//Напишите программу, которая вычисляет сумму квадратов чисел от 1 до N.Число N
//программа должна запрашивать у пользователя
#include <stdio.h>
#include <locale.h>
int main() {
    char* locale = setlocale(LC_ALL, "rus");

    int N; // Переменная для хранения значения N
    int sum = 0; // Переменная для хранения суммы квадратов

    // Запрос числа N у пользователя
    printf("Введите число N: ");
    #pragma warning(suppress : 4996)
    scanf("%d", &N);

    // Проверка на корректность ввода
    if (N <= 0) {
        printf("Число N должно быть положительным.\n");
        return 1;
    }

    // Вычисление суммы квадратов
    for (int i = 1; i <= N; i++) {
        sum += i * i; // Добавляем квадрат текущего числа к сумме
    }

    // Вывод результата
    printf("Сумма квадратов чисел от 1 до %d равна: %d\n", N, sum);

    return 0;
}
