//Пользователь вводит три числа – длины сторон треугольника.Программа должна
//сообщить пользователю, каким является треугольник : равносторонним,
//равнобедренным, разносторонним или прямоугольным, а также существует ли
//вообще такой треугольник(такого треугольника не может быть, если сумма любых
//	двух сторон окажется меньше третьей стороны).
#include <stdio.h>
#include <math.h>

int main() {
    double a, b, c;

    // ввод сторон треугольника
    printf("Введите длины сторон треугольника (a, b, c):\n");
    scanf("%lf %lf %lf", &a, &b, &c);

    // проверка существования треугольника
    if (a <= 0 || b <= 0 || c <= 0 || (a + b <= c) || (a + c <= b) || (b + c <= a)) {
        printf("Такого треугольника не существует.\n");
    }
    else {
        // определение типа треугольника
        if (a == b && b == c) {
            printf("Треугольник равносторонний.\n");
        }
        else if (a == b || a == c || b == c) {
            printf("Треугольник равнобедренный.\n");
        }
        else {
            printf("Треугольник разносторонний.\n");
        }

        // проверка на прямоугольный треугольник
        double max_side = fmax(a, fmax(b, c));
        double sum_of_squares = a * a + b * b + c * c;

        if (max_side * max_side == sum_of_squares - max_side * max_side) {
            printf("Треугольник является прямоугольным.\n");
        }
    }

    return 0;
}
